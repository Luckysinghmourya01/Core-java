package com.rays.oppInheritance;

public class Dog extends Animal {
	
	private int age;
	private String eat;
	
	public void setAge(int age) {
		this.age = age;
	}
	
	public int getAge() {
		return this.age;
	}
	
	public void setEat(String eat) {
		this.eat = eat;
	}
	public String getEat() {
		return this.eat;
	}

}

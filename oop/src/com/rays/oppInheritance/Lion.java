package com.rays.oppInheritance;

public class Lion extends Animal {
	
	private String eat;
	private String skill;
	
	public void setEat(String eat) {
		this.eat = eat;
	}
	
	public String getEat() {
		return this.eat;
	}

	public void setSkill(String skill) {
		this.skill = skill;
	}
	public String getSkill() {
		return this.skill;
	}
}

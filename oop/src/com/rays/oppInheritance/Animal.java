package com.rays.oppInheritance;

public class Animal {
	
	protected String name;
	protected String sound;
	
	public void setName(String name) {
		this.name = name;
	}
	public String getName() {
		return this.name;
	}
	
	public void setSound(String sound) {
		this.sound = sound;
	}
	
	public String getSound() {
		return this.sound;
	}

}
